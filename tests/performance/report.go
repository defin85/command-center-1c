// PerformanceReport - отчет о performance тесте
type PerformanceReport struct {
	TestName        string        `json:"test_name"`
	Timestamp       time.Time     `json:"timestamp"`
	TotalOperations int           `json:"total_operations"`
	SuccessCount    int           `json:"success_count"`
	FailureCount    int           `json:"failure_count"`
	TotalDuration   time.Duration `json:"total_duration"`
	OpsPerSecond    float64       `json:"ops_per_second"`
	SuccessRate     float64       `json:"success_rate"` // 0.0-1.0 fraction
	LatencyMin      float64       `json:"latency_min_seconds"`
	LatencyMean     float64       `json:"latency_mean_seconds"`
	LatencyP50      float64       `json:"latency_p50_seconds"`
	LatencyP95      float64       `json:"latency_p95_seconds"`
	LatencyP99      float64       `json:"latency_p99_seconds"`
	LatencyMax      float64       `json:"latency_max_seconds"`
	Latencies       []time.Duration `json:"latencies,omitempty"`
}

