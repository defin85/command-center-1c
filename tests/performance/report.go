// GenerateMarkdownReport - генерирует markdown отчет из PerformanceReport
func GenerateMarkdownReport(report *PerformanceReport, filename string) error {
	// Create template with custom functions
	funcMap := template.FuncMap{
		"SuccessRatePercent": func() float64 {
			return report.SuccessRate * 100.0
		},
	}

	tmpl, err := template.New("report").Funcs(funcMap).Parse(reportTemplate)
	if err != nil {
		return fmt.Errorf("failed to parse template: %w", err)
	}

	f, err := os.Create(filename)
	if err != nil {
		return fmt.Errorf("failed to create file: %w", err)
	}
	defer f.Close()

	if err := tmpl.Execute(f, report); err != nil {
		return fmt.Errorf("failed to execute template: %w", err)
	}

	return nil
}

// GenerateComparisonReport - генерирует markdown отчет сравнения
